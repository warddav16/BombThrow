void DX11Manager::StartWindow(HWND hwnd, int width, int height, bool fullscreen)
{
	DXGI_SWAP_CHAIN_DESC scd;

	m_screenDepth = 1000.0f;
	m_screenNear = .1f;

    // clear out the struct for use
    ZeroMemory(&scd, sizeof(DXGI_SWAP_CHAIN_DESC));

    // fill the swap chain description struct
    scd.BufferCount = 1;                                    // one back buffer
    scd.BufferDesc.Format = DXGI_FORMAT_R8G8B8A8_UNORM;     // use 32-bit color
    scd.BufferUsage = DXGI_USAGE_RENDER_TARGET_OUTPUT;      // how swap chain is to be used
    scd.OutputWindow = hwnd;                                // the window to be used
    scd.SampleDesc.Count = 4;                               // how many multisamples
	scd.Windowed = !fullscreen;                             // windowed/full-screen mode
	m_width = width;
	m_height = height;
	m_fullscreen = fullscreen;
	m_hwnd = hwnd;

    // create a device, device context and swap chain using the information in the scd struct
    D3D11CreateDeviceAndSwapChain(NULL,
                                  D3D_DRIVER_TYPE_HARDWARE,
                                  NULL,
                                  NULL,
                                  NULL,
                                  NULL,
                                  D3D11_SDK_VERSION,
                                  &scd,
                                  &m_swapChain,
                                  &m_device,
                                  NULL,
                                  &m_deviceContext);

	if(!InitDx3d())
	{
		MessageBox(hwnd, L"Fuckin crashed", L"Error", MB_OK);
	}
}

void DX11Manager::CloseWindow()
{
	// Before shutting down set to windowed mode or when you release the swap chain it will throw an exception.
	if(m_swapChain)
	{
		m_swapChain->SetFullscreenState(false, NULL);
	}

	if(m_rasterState)
	{
		m_rasterState->Release();
		m_rasterState = 0;
	}

	if(m_depthStencilView)
	{
		m_depthStencilView->Release();
		m_depthStencilView = 0;
	}

	if(m_depthStencilState)
	{
		m_depthStencilState->Release();
		m_depthStencilState = 0;
	}

	if(m_depthStencilBuffer)
	{
		m_depthStencilBuffer->Release();
		m_depthStencilBuffer = 0;
	}

	if(m_renderTargetView)
	{
		m_renderTargetView->Release();
		m_renderTargetView = 0;
	}

	if(m_deviceContext)
	{
		m_deviceContext->Release();
		m_deviceContext = 0;
	}

	if(m_device)
	{
		m_device->Release();
		m_device = 0;
	}

	if(m_swapChain)
	{
		m_swapChain->Release();
		m_swapChain = 0;
	}

}

void DX11Manager::RenderFrame(list<GameObject*> gameObjects)
{
	list<GameObject*>::iterator iter = gameObjects.begin();
	for(; iter != gameObjects.end(); iter++)
	{
		if((*iter)->GetRenderer() != NULL)
		{
			(*iter)->GetRenderer()->Render();
		}
	}
}

